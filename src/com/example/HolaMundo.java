package com.example;

public class HolaMundo {
    //  Public: quiere decir que este metodo es publico
    //Static: es un modificador que indica que este metodo es solamente de esta clase "HolaMundo"
    //Void: Es un tipo de retorno de una funcion que indica que no retorna nada esta función.
    //Es un metodo o función que siempre debe estar dentro de la clase.
    //String[]: Indica que el método main recibe un array de objetos de tipo String (cadenas de texto).
    //args: Es simplemente un nombre para el parámetro. Podrías llamarlo de otra forma, pero por convención, se usa args.
    public static void main(String[] args) {
        System.out.println("Hola Mundo, este es el comienzo con JAVA!");
    }
}
